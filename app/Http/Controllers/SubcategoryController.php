    public function getSubByCat($id){
        $cate = DB::table('subcategories')
            ->join('categories', 'categories.id', '=', 'subcategories.cat_id')
            ->select('subcategories.*')
            ->where('subcategories.cat_id',$id)
            ->get();
        return response($cate,200);
